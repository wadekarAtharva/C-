// C++ Program to Calculate Area of Rectangle.

#include<iostream>
using namespace std;


int main(){
        double lenght,breadth,area;

        cout<<"Enter length:";
        cin>>lenght;

        cout<<"Enter breadth:";
        cin>>breadth;

        area=lenght*breadth;

        cout<<"The area of rectangle is "<<area<<endl;
return 0;

}